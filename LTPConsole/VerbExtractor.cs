        public static FunctionSegment[]? Extract(string content)
#endif
        {
            // 检查参数
            if (content == null ||
                content.Length <= 0) return null;

#if _USE_CLR
            // 获得匹配的项目
            FunctionSegment[] output =
#elif _USE_CONSOLE
            // 获得匹配的项目
